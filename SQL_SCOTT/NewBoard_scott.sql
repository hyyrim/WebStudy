--○ TBL_NEWBOARD 테이블 생성 (게시판 전용 테이블 생성)
CREATE TABLE TBL_NEWBOARD
( NUM       NUMBER(9)               NOT NULL   
, NAME      VARCHAR2(30)            NOT NULL
, PWD       VARCHAR2(20)            NOT NULL
, EMAIL     VARCHAR2(50)
, SUBJECT   VARCHAR2(100)           NOT NULL
, CONTENT   VARCHAR2(4000)          NOT NULL
, IPADDR    VARCHAR2(20)
, HITCOUNT  NUMBER DEFAULT 0        NOT NULL
, CREATED   DATE DEFAULT SYSDATE    NOT NULL
, CONSTRAINT NEWBOARD_NUM_PK PRIMARY KEY(NUM)
);
--==> Table TBL_NEWBOARD이(가) 생성되었습니다.

